// Declares a myAge variable with let, with the value of the variable equal to 24
let myAge = 24;
// Declares an earlyYears variable with let, with the value of the variable equal to 2. Note this value can change
let earlyYears = 2;
earlyYears *= 10.5;
// Variable laterYears is equal to the value of variable myAge
let laterYears = myAge - 2;
// Use of the multiplication assignment operator to multiply the variable laterYears by a value of 4.
laterYears *= 4

// Variable representing human age in dog age(years), calculated by adding the variables earlyYears and laterYears together
myAgeInDogYears = earlyYears + laterYears;
// Variable myName has a stored value of "Matthew" as astring, the built-in method .toLowerCase is called and this meethod returns a string of all lowercase letters.
myName = "Matthew".toLowerCase();

// String Interpolation
console.log(`My name is ${myName}, I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)


